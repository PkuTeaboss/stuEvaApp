    @IBAction func returned(segue: UIStoryboardSegue) {
        tableView.reloadData()
    }

    // MARK: - Table view data source

    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        // #warning Potentially incomplete method implementation.
        // Return the number of sections.
        return 1
    }

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete method implementation.
        // Return the number of rows in the section.
        return data.count
    }

    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("IDCell", forIndexPath: indexPath) as! MainTableViewCell

        // Configure the cell...
        let row = indexPath.row
        
        //println(row)
        //println(data[0].id)
        //println(data[0].name)
        cell.nameLabel.text = data[row].xingming
        cell.idLabel.text = data[row].xuehao
        cell.statusLabel.text = data[row].status

        return cell
    }
    
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        if segue.identifier == "ShowDetail" {
            if let indexPath = tableView.indexPathForSelectedRow() {
                let object = data[indexPath.row]
                (segue.destinationViewController as! DetailTableViewController).detail = object
                (segue.destinationViewController as! DetailTableViewController).navigationItem.title = object.xingming
                
            }
            
        }
    }

